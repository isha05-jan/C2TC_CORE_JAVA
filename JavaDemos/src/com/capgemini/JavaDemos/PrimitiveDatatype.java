package com.capgemini.JavaDemos;

public class PrimitiveDatatype {

	public static void main(String[] args) {
		 //integer max variable value define
		   int maxLimit = 2147483647;
		   
		   //integer minimum variable value define
		   int minLimit = -2147483648;
		   
		   //short maximum variable value is define
		   short shortMax = 32767;
		   
		   //short minimum variable value is define
		   short shortMin = -32768;
		   
		   //long maximum variable value is define
		   long longMax = 9223372036854775807L;
		   
		   //long minimum variable value is define
		   long longMin = -9223372036854775808L;
		   
		   //byte maximum variable value is define
		   byte byteMax = 127;
		   
		   //byte minimum variable value is define
		   byte byteMin = -128;
		   
		   //print the maxLimit value
		   System.out.println("Max Limit of Integer Value Is :"+ maxLimit);
		  
		   //print the minLimit value

		   System.out.println("Min Limit of Integer Value Is :"+ minLimit);
		 
		   //print the shortMax value

		   System.out.println("Short Max Value Is :"+ shortMax);
		   
		   //print the shortMin value

		   System.out.println("Short Min Value Is :"+ shortMin);
		   
		   //print the longMax value

		   System.out.println("Long Max Value Is :"+ longMax);

		   //print the longMin value

		   System.out.println("Long Min Value Is :"+ longMin);

		   //print the byteMax value

		   System.out.println("Byte Max Value Is :"+ byteMax);

		  
		   //print the byteMin value

		   System.out.println("Byte Min Value Is :"+ byteMin);
		// TODO Auto-generated method stub

	}

}
